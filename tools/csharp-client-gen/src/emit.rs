/// The exceptions, the per-method request records, and `GameBackendClient`.
pub fn emit_client(m: &Manifest) -> String {
    let mut s = String::new();
    s.push_str(HEADER);
    s.push('\n');
    s.push_str("using System.Text.Json;\n");
    s.push_str("using System.Text.Json.Nodes;\n");
    s.push_str("using System.Text.Json.Serialization;\n");
    s.push_str("using GameBackend.Client.Transport;\n\n");
    s.push_str(&format!("namespace {NS};\n\n"));

    // --- Exceptions ---
    s.push_str(
        "/// <summary>A transport-level fault (framing / envelope / unwired front): the\n\
         /// player response arrived with <c>ok == false</c>, or the envelope was malformed.</summary>\n",
    );
    s.push_str(
        "public sealed class GameBackendTransportException(string? error)\n\
         \x20   : Exception(error ?? \"player transport fault\");\n\n",
    );
    s.push_str(
        "/// <summary>A completed operation whose domain <c>status</c> was not <c>Ok</c>.\n\
         /// <see cref=\"Status\"/> is the outcome; <see cref=\"Detail\"/> is the server's <c>err</c>.</summary>\n",
    );
    s.push_str(
        "public sealed class GameBackendStatusException(Status status, string? detail)\n\
         \x20   : Exception($\"operation failed with status {status}: {detail ?? \"<no detail>\"}\")\n\
         {\n\
         \x20   /// <summary>The non-<c>Ok</c> domain status.</summary>\n\
         \x20   public Status Status { get; } = status;\n\
         \x20   /// <summary>The server-supplied error detail, if any.</summary>\n\
         \x20   public string? Detail { get; } = detail;\n\
         }\n\n",
    );

    // --- Request records (one per method, in manifest order) ---
    for method in &m.methods {
        s.push_str(&emit_request_record(method));
        s.push('\n');
    }

    // --- The client ---
    s.push_str(
        "/// <summary>\n\
         /// The generated, typed player client. Each method builds a request DTO (always a\n\
         /// concrete object — a no-arg call serializes to <c>{}</c>, never <c>null</c>),\n\
         /// dispatches it over <see cref=\"IPlayerTransport\"/>, then unwraps the domain\n\
         /// envelope: a transport fault throws <see cref=\"GameBackendTransportException\"/>, a\n\
         /// non-<c>Ok</c> status throws <see cref=\"GameBackendStatusException\"/>.\n\
         /// </summary>\n",
    );
    s.push_str("public sealed class GameBackendClient(IPlayerTransport transport)\n{\n");
    s.push_str(
        "    private static readonly JsonSerializerOptions JsonOpts = new()\n\
         \x20   {\n\
         \x20       Converters = { new JsonStringEnumConverter() },\n\
         \x20   };\n\n",
    );

    for (i, method) in m.methods.iter().enumerate() {
        if i > 0 {
            s.push('\n');
        }
        s.push_str(&emit_method(method));
    }

    // --- Shared unwrap helper ---
    s.push('\n');
    s.push_str(
        "    /// <summary>Validates the transport flag and the domain status, returning the\n\
         \x20   /// payload envelope on success.</summary>\n\
         \x20   private static JsonNode Unwrap(PlayerResponse resp)\n\
         \x20   {\n\
         \x20       if (!resp.Ok)\n\
         \x20           throw new GameBackendTransportException(resp.Error);\n\
         \x20       JsonNode payload = resp.Payload\n\
         \x20           ?? throw new GameBackendTransportException(\"transport reported ok but no payload envelope\");\n\
         \x20       JsonNode statusNode = payload[\"status\"]\n\
         \x20           ?? throw new GameBackendTransportException(\"response envelope missing 'status'\");\n\
         \x20       Status status = statusNode.Deserialize<Status>(JsonOpts);\n\
         \x20       if (status != Status.Ok)\n\
         \x20           throw new GameBackendStatusException(status, payload[\"err\"]?.GetValue<string>());\n\
         \x20       return payload;\n\
         \x20   }\n",
    );
    s.push_str("}\n");
    s
}

/// The request `record` for one method — one positional param per arg (empty when none,
/// which serializes to `{}`). `[property: JsonPropertyName]` carries the wire key.
fn emit_request_record(method: &MethodDef) -> String {
    let name = format!("{}Request", op_base(method));
    if method.args.is_empty() {
        return format!(
            "/// <summary>Request for <c>{}</c> (no arguments — serializes to <c>{{}}</c>).</summary>\n\
             public sealed record {name}();\n",
            method.wire_method
        );
    }
    let members: Vec<String> = method
        .args
        .iter()
        .map(|a| {
            format!(
                "    [property: JsonPropertyName(\"{}\")] {} {}",
                a.wire_name,
                cs_type(&a.ty),
                pascal_from_snake(&a.name)
            )
        })
        .collect();
    format!(
        "/// <summary>Request for <c>{}</c>.</summary>\n\
         public sealed record {name}(\n{});\n",
        method.wire_method,
        members.join(",\n")
    )
}

/// One typed async client method.
fn emit_method(method: &MethodDef) -> String {
    let ret = return_type(&method.ret);
    let mut params: Vec<String> = Vec::new();
    let player = method.auth == "player";
    if player {
        params.push("string token".to_string());
    }
    for a in &method.args {
        params.push(format!("{} {}", cs_type(&a.ty), escape_ident(&camel_from_snake(&a.name))));
    }
    params.push("CancellationToken ct = default".to_string());

    let req_name = format!("{}Request", op_base(method));
    let ctor_args: Vec<String> = method
        .args
        .iter()
        .map(|a| escape_ident(&camel_from_snake(&a.name)))
        .collect();
    let token_expr = if player { "token" } else { "null" };

    let mut s = String::new();
    s.push_str(&format!(
        "    /// <summary>Invokes <c>{}</c> ({}).</summary>\n",
        method.wire_method,
        if player { "requires a bearer token" } else { "unauthenticated" }
    ));
    s.push_str(&format!(
        "    public async {ret} {}Async({})\n    {{\n",
        op_base(method),
        params.join(", ")
    ));
    s.push_str(&format!(
        "        var request = new {req_name}({});\n",
        ctor_args.join(", ")
    ));
    s.push_str("        byte[] payload = JsonSerializer.SerializeToUtf8Bytes(request, JsonOpts);\n");
    s.push_str(&format!(
        "        PlayerResponse resp = await transport.CallAsync(\"{}\", {token_expr}, payload, ct).ConfigureAwait(false);\n",
        method.wire_method
    ));
    match &method.ret {
        TypeRef::Unit => {
            s.push_str("        Unwrap(resp);\n");
        }
        _ => {
            let cs = cs_type(&method.ret);
            s.push_str("        JsonNode envelope = Unwrap(resp);\n");
            s.push_str(&format!(
                "        JsonNode value = envelope[\"value\"]\n\
                 \x20           ?? throw new GameBackendTransportException(\"Ok response missing 'value'\");\n\
                 \x20       return value.Deserialize<{cs}>(JsonOpts)!;\n"
            ));
        }
    }
    s.push_str("    }\n");
    s
}

// ---------------------------------------------------------------------------
// Naming + type helpers
// ---------------------------------------------------------------------------

/// `<PascalProvider><CapFirst(suffix)>` — the shared base for a method's C# name and its
/// request record. `characters.create` → `CharactersCreate`; `inventory.listMine` →
/// `InventoryListMine`.
fn op_base(method: &MethodDef) -> String {
    let suffix = method
        .wire_method
        .split_once('.')
        .map(|(_, s)| s)
        .unwrap_or(&method.wire_method);
    format!("{}{}", pascal_from_snake(&method.provider), cap_first(suffix))
}

/// The C# `Task`/`Task<T>` return type for a method's return `TypeRef`.
fn return_type(ret: &TypeRef) -> String {
    match ret {
        TypeRef::Unit => "Task".to_string(),
        other => format!("Task<{}>", cs_type(other)),
    }
}

/// Maps a value [`TypeRef`] to its C# type. `Unit` is not a value type — callers that can
/// see it (`return_type`) handle it before calling here.
fn cs_type(ty: &TypeRef) -> String {
    match ty {
        TypeRef::String => "string".to_string(),
        TypeRef::I64 => "long".to_string(),
        TypeRef::Struct(name) => name.clone(),
        TypeRef::Vec(inner) => format!("{}[]", cs_type(inner)),
        TypeRef::Unit => "void".to_string(),
    }
}

/// `player_id` → `PlayerId`, `class` → `Class`, `topScores`/`create` unchanged-cased per
/// segment (already-camel single segments just get their first letter capitalized).
fn pascal_from_snake(s: &str) -> String {
    s.split('_')
        .filter(|p| !p.is_empty())
        .map(cap_first)
        .collect()
}

/// `player_id` → `playerId`, `character_id` → `characterId`, `class` → `class`.
fn camel_from_snake(s: &str) -> String {
    lower_first(&pascal_from_snake(s))
}

fn cap_first(s: &str) -> String {
    let mut c = s.chars();
    match c.next() {
        Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
        None => String::new(),
    }
}

fn lower_first(s: &str) -> String {
    let mut c = s.chars();
    match c.next() {
        Some(f) => f.to_lowercase().collect::<String>() + c.as_str(),
        None => String::new(),
    }
}

/// Prefixes a C# reserved keyword with `@` so it is a legal identifier (e.g. the
/// `class` arg → `@class`).
fn escape_ident(name: &str) -> String {
    if CS_KEYWORDS.contains(&name) {
        format!("@{name}")
    } else {
        name.to_string()
    }
}

/// The C# reserved keywords (identifiers that must be `@`-escaped when reused as a name).
const CS_KEYWORDS: &[&str] = &[
    "abstract", "as", "base", "bool", "break", "byte", "case", "catch", "char", "checked",
    "class", "const", "continue", "decimal", "default", "delegate", "do", "double", "else",
    "enum", "event", "explicit", "extern", "false", "finally", "fixed", "float", "for",
    "foreach", "goto", "if", "implicit", "in", "int", "interface", "internal", "is", "lock",
    "long", "namespace", "new", "null", "object", "operator", "out", "override", "params",
    "private", "protected", "public", "readonly", "ref", "return", "sbyte", "sealed", "short",
    "sizeof", "stackalloc", "static", "string", "struct", "switch", "this", "throw", "true",
    "try", "typeof", "uint", "ulong", "unchecked", "unsafe", "ushort", "using", "virtual",
    "void", "volatile", "while",
];
