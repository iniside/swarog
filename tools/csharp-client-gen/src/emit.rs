//! The emit phase (Step 3): turn the scraped [`Manifest`] into deterministic C# under
//! `clients/csharp/Generated/`. Three files, namespace `GameBackend.Client.Generated`:
//!
//! - `Status.cs` — the `Status` enum (variant names verbatim; round-trips via
//!   `JsonStringEnumConverter`).
//! - `Dtos.cs` — one positional `record` per DTO, `[property: JsonPropertyName]` on
//!   EVERY field (the wire keys are snake_case and `System.Text.Json` is case-sensitive).
//! - `Client.cs` — the two exception types, one request `record` per method (empty ⇒
//!   serializes to `{}`, never `null` — the Step-1 live-verified rule), and
//!   `GameBackendClient(IPlayerTransport)` with one `async` method per reachable op.
//!
//! Output is byte-stable: fixed header (no timestamp/version), methods in the manifest's
//! sorted order, fields in declaration order, LF newlines. Step 5's freshness gate
//! (`git diff --exit-code`) depends on this.
//!
//! Method-naming scheme: `<PascalProvider><CapFirst(methodSuffix)>Async`, e.g.
//! `characters.create` → `CharactersCreateAsync`, `inventory.listMine` →
//! `InventoryListMineAsync`, `leaderboard.topScores` → `LeaderboardTopScoresAsync`. The
//! provider prefix disambiguates same-named ops across providers (two `list`s).

use std::path::Path;

use anyhow::{Context as _, Result};

use crate::model::{DtoDef, Manifest, MethodDef, TypeRef};

const NS: &str = "GameBackend.Client.Generated";

/// Fixed provenance header prepended to every emitted file — NO timestamp/version so the
/// output is byte-stable for the freshness gate.
const HEADER: &str = "// <auto-generated>\n\
    //     Generated by tools/csharp-client-gen. DO NOT EDIT BY HAND.\n\
    //     Regenerate: cargo run -p csharp-client-gen -- --out clients/csharp/Generated\n\
    // </auto-generated>\n\
    \n\
    // The <auto-generated> marker above puts this file OUTSIDE the project nullable\n\
    // context, so the annotations below need an explicit directive (else CS8669).\n\
    #nullable enable\n";

/// Emits the three C# files into `out_dir`, creating it if needed.
pub fn emit(manifest: &Manifest, out_dir: &Path) -> Result<()> {
    std::fs::create_dir_all(out_dir)
        .with_context(|| format!("create output dir {}", out_dir.display()))?;
    for (name, body) in [
        ("Status.cs", emit_status(manifest)),
        ("Dtos.cs", emit_dtos(manifest)),
        ("Client.cs", emit_client(manifest)),
    ] {
        let path = out_dir.join(name);
        std::fs::write(&path, body).with_context(|| format!("write {}", path.display()))?;
    }
    Ok(())
}

