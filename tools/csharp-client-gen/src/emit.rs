/// The `Status` enum, variants in declaration order. The wire form is the exact variant
/// name, so a `JsonStringEnumConverter` round-trips it with no `[EnumMember]` renames.
pub fn emit_status(m: &Manifest) -> String {
    let mut s = String::new();
    s.push_str(HEADER);
    s.push('\n');
    s.push_str(&format!("namespace {NS};\n\n"));
    s.push_str("/// <summary>\n");
    s.push_str("/// The domain outcome taxonomy (mirrors <c>opsapi::Status</c>). The wire form is the\n");
    s.push_str("/// exact variant name; a <c>JsonStringEnumConverter</c> round-trips it.\n");
    s.push_str("/// </summary>\n");
    s.push_str("public enum Status\n{\n");
    for v in &m.statuses {
        s.push_str(&format!("    {v},\n"));
    }
    s.push_str("}\n");
    s
}

